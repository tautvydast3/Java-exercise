package application;

public class App {

	public static void main(String[] args) {
		
		// To use slash (\) in printable text, I must put two slashes (\\). Otherwise Java interprets slash as a special character, like \t or \n.
		
		
		  String text = "\tTo be \\ or not to be.\n\tThat is the question."; 
		  		System.out.println(text);
		  		
	}

}
